import { Vector } from "../Objects/SpriteTypes";

export type MapNames = "startingPoint" | "smallTown";
export type Teleports = {
  initial: Vector; // starts 0-0 to be filled later in buildGameEntities
  destination: Vector;
  newMapName: MapNames;
};
export type MapData = {
  mapName: MapNames;
  mapString: string;
  entityString: string;
  teleports: Teleports;
  initalOffset: Vector;
};

function createMapMetaData(LevelParams: {
  mapName: "startingPoint" | "smallTown";
  lastScreenOffset: Vector;
  newScreenOffset: Vector;
}): MapData {
  const maps: { [key in MapNames]: MapData } = {
    startingPoint: {
      mapName: "startingPoint",
      mapString: Map1,
      entityString: EntitiesMap1,
      teleports: {
        initial: { x: 0, y: 0 },
        destination: { x: 2, y: -11 },
        newMapName: "smallTown",
      },

      initalOffset: LevelParams.newScreenOffset,
    },
    smallTown: {
      mapName: "smallTown",
      mapString: Map2,
      entityString: EntitiesMap2,
      teleports: {
        initial: { x: 0, y: 0 },
        destination: { x: 0, y: 0 },
        newMapName: "startingPoint",
      },

      initalOffset: LevelParams.newScreenOffset,
    },
  };
  const selection = maps[LevelParams.mapName];
  return selection;
}

